/**
 * 教师类
 */
public class Teacher {
    private String name;
    private String subject;

    /**
     * @return 姓名
     */
    public String getName() {
        return name;
    }

    /**
     * @param name 设置姓名
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return 任教科目
     */
    public String getSubject() {
        return subject;
    }

    /**
     * @param subject 设置任教科目
     */
    public void setSubject(String subject) {
        this.subject = subject;
    }
}
